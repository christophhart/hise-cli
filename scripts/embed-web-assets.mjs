// ── Embed dist/web/* into a TS module for bun --compile ─────────────
//
// Walks dist/web/ and emits src/web/embedded-assets.ts: a map from
// URL path → base64-encoded file bytes. The web server reads from
// this map instead of the filesystem so the compiled binary is fully
// self-contained.
//
// If dist/web/ is missing (typecheck before build), writes an empty
// stub so tsc succeeds.

import {
	mkdirSync,
	readFileSync,
	readdirSync,
	statSync,
	writeFileSync,
	existsSync,
} from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const webDir = resolve(root, "dist", "web");
const outFile = resolve(root, "src", "web", "embedded-assets.ts");

function walk(dir, files = []) {
	for (const entry of readdirSync(dir)) {
		const full = join(dir, entry);
		if (statSync(full).isDirectory()) walk(full, files);
		else files.push(full);
	}
	return files;
}

let entries = [];
if (existsSync(webDir)) {
	entries = walk(webDir).map((file) => {
		const rel = "/" + relative(webDir, file).replace(/\\/g, "/");
		const b64 = readFileSync(file).toString("base64");
		return [rel, b64];
	});
}

const lines = [
	"// AUTO-GENERATED by scripts/embed-web-assets.mjs — do not edit.",
	"// Regenerated each build.",
	"",
	"const _data: Record<string, string> = {",
	...entries.map(([k, v]) => `\t${JSON.stringify(k)}: ${JSON.stringify(v)},`),
	"};",
	"",
	"function decode(b64: string): Uint8Array {",
	"\treturn Uint8Array.from(atob(b64), (c) => c.charCodeAt(0));",
	"}",
	"",
	"export const embeddedAssets: Map<string, Uint8Array> = new Map(",
	"\tObject.entries(_data).map(([k, v]) => [k, decode(v)] as const),",
	");",
	"",
];

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, lines.join("\n"));

const totalBytes = entries.reduce(
	(sum, [, v]) => sum + Math.floor((v.length * 3) / 4),
	0,
);
console.log(
	`[embed-web-assets] embedded ${entries.length} files (${(totalBytes / 1024 / 1024).toFixed(1)} MB) → ${relative(root, outFile)}`,
);
